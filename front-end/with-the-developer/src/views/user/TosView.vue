                                제 13 조 (매매보호장치)
                                회사의 서비스를 통하여 이루어지는 프로젝트 또는 서비스 거래에 관한 결제 과정 및 반품처리 과정을 위하여 매매 보호 장치인 에스크로 제도를 실행하고 있으며, 바이어 회원과 프리랜서 회원은 상호 협의하여 에스크로 제도를 도입할 수 있습니다.
                                에스크로 제도의 기능 및 목적은 아래와 같습니다.
                                1. <br><br>바이어 회원이 프리랜서 회원으로부터 공급받은 프로젝트 성과물에 대한 하자 문제 등의 담보
                                2. <br><br>프리랜서 회원이 프로젝트 성과물을 바이어 회원에게 제공하고도 그에 대한 대금을 받지 못할 경우의 담보
                                에스크로 제도의 절차 - 일반형 서비스의 경우
                                에스크로 제도의 절차는 바이어 회원이 프로젝트 대금을 회사에 선지급하고, 바이어 회원이 프리랜서 회원으로부터 프로젝트 최종 성과물이나 중간 성과물을 정상적으로 인도받은 즉시 회사에 지급 확인서를 교부하면 회사는 지급 확인서를 교부 받은 즉시 3일 이내에 바이어 회원이 프리랜서에게 지급하기로 한 일자에 프로젝트 대금을 프리랜서 회원에게 지급합니다.
                                에스크로 제도의 예치금 제도는 바이어 회원과 프리랜서 회원이 협의하여 총 프로젝트 비용의 30%에 해당하는 금액을 회사가 예치 (분할 지급할 경우에는 분할 지급액의 30%에 해당하는 금액을 계속 예치)하고 있다가 프로젝트 종료 이후 바이어 회원이 검수 기간 내에 검수를 종료하고, 검수 확인서를 회사에 교부할 경우나 검수 확정 기일이 도래할 경우에는 회사는 3일 이내에 예치금 중에서 총 프로젝트 비용의 20%에 해당하는 금액을 프리랜서 회원에게 지급하며, 총 프로젝트 비용의 10%에 해당하는 마지막 예치금은 1개월의 무상 하자 보증 기간이 도래함과 동시에 프리랜서 회원에게 지급하는 제도입니다.
                                무상 하자 보증 기간이 1개월 이상일 경우 회사가 프리랜서 회원에게 총 프로젝트 비용의 10%에 해당하는 최종적인 예치금을 지급하였더라도 도래하지 않는 무상하자 보증기간 동안의 하자보수를 특별한 사유 없이 게을리하여 그 책임을 다 하지 못할 경우에는 하자보수보증보험적 성격을 가진 총 프로젝트 비용의 10%에 해당하는 금액을 프리랜서 회원이 바이어 회원에게 반환하여야 합니다.
                                에스크로 제도 도입 시, 3개월 이상의 중, 장기 프로젝트로 프리랜서 회원이 바이어 회원의 본점 소재지 및 바이어 회원의 프로젝트 진행지에서 상주로 프로젝트를 진행하면서 프로젝트 대금을 매월 정산하기로 바이어 회원과 프리랜서 회원이 합의한 경우에는 1회차 프로젝트 대금 결산 시에 회사가 바이어 회원으로부터 지급 확인서를 교부 받으면 예치금 30%를 공제한 금액을 지급하며, 2회차 프로젝트 대금 결산 시에 회사가 바이어 회원으로부터 지급확인서를 교부 받으면 전월(1회차) 예치금 중에서 전월 지급액의 20%에 해당하는 금액과 2회차 프로젝트 대금 지급액 중 30%의 예치금을 제외한 금액을 지급하며, 프로젝트가 종료되어 바이어 회원이 검수 및 지급 확인서를 회사에 교부하면 마지막차 지급일 때에는 마지막차 지급액 중 10%에 해당하는 금액만을 예치하고, 전월 지급 금액 중 20%에 해당하는 금액과 함께 지급하며, 최종적으로 적립되어 있는 총 프로젝트 비용 중 10%에 해당하는 금액은 3개월의 무상하자보증기간이 종료됨과 동시에 프리랜서 회원에게 지급하는 제도입니다.
                                매매보호장치인 에스크로 제도에 따른 프로젝트 또는 서비스의 제공 결과로 회사가 취득하게 되는 이자 등의 이익은 서비스 제공의 대가로서의 성질을 가지는 것이며, 바이어 회원과 프리랜서 회원은 이러한 이자 등에 대한 권리를 주장하지 않습니다. <br><br>또한, 송금 시 발생하는 수수료에 대해서는 회원 본인이 부담하는 것으로 이를 회사에 청구할 수 없습니다.
                                에스크로 제도의 절차 - 재택(턴키) 서비스의 경우
                                에스크로 제도의 절차는 바이어 회원이 계약 체결과 동시에 프로젝트 대금을 회사에 선지급하고, 바이어 회원이 프리랜서 회원으로부터 프로젝트 최종 성과물이나 중간 성과물을 정상적으로 인도받은 즉시 회사에 지급을 요청하면 회사는 지급 요청을 받은 날로부터 15일 이내에 대금을 프리랜서 회원에게 지급합니다.
                                에스크로 제도의 예치금 제도는 바이어 회원과 프리랜서 회원이 합의한 프로젝트 대금을 회사가 예치하고 있다가 프로젝트 종료 이후 바이어 회원이 검수 기간 내에 검수를 종료하고, 바이어가 회사에 지급을 요청한 날로부터 15일 이내에 회사가 프로젝트 대금을 프리랜서에게 지급하는 제도입니다.
                                계약기간 종료 여부와 무관하게 바이어가 회사에 프로젝트 지급 요청이 없을 경우, 회사는 프로젝트 대금을 지급하지 않습니다.
                                회사가 회사에 예치한 프로젝트 대금을 환불을 요청할 경우, 계약 당사자인 프리랜서가 서면 또는 본인의 의사를 표시할 수 있는 수단으로 회사에 환불을 승인할 경우에 회사는 프로젝트 대금을 바이어에게 환불합니다.
                                매매보호장치인 에스크로 제도에 따른 프로젝트 또는 서비스의 제공 결과로 회사가 취득하게 되는 이자 등의 이익은 서비스 제공의 대가로서의 성질을 가지는 것이며, 바이어 회원과 프리랜서 회원은 이러한 이자 등에 대한 권리를 주장하지 않습니다. <br><br>또한, 송금 또는 타 금융기관을 통한 에스크로 서비스 이용 시 발생하는 수수료에 대해서는 회원 본인이 부담하는 것으로 이를 회사에 청구할 수 없습니다.
