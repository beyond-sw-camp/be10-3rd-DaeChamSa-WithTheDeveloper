                                바이어 회원은 입찰에 참가한 프리랜서 회원 및 회사 담당자를 통하여 추천 받은 프리랜서 회원에 대하여 서류전형, 면접전형 등의 합격, 불합격 여부와 합격, 불합격 사유를 회사측에 고지하여야 합니다.
                                바이어 회원은 프리랜서 회원을 선정한 후에는 제13조 회사의 매매 보호 장치인 에스크로 제도의 도입여부를 상호 협의하여야 하며, 에스크로 제도를 도입하지 않을 경우에는 바이어 회원과 프리랜서 회원 및 회사가 상호 협의하여 프로젝트 총 비용에 대한 입금일시, 입금방법 등의 입금정보를 결정합니다. <br><br>이때, 바이어 회원과 프리랜서 회원은 입금방법, 입금은행, 입금자명, 입금일시 등 입금정보를 전화, 이메일 혹은 서비스 화면에 기입해야 하며, 이를 잘못 기입하거나 기입하지 않음으로 인하여 발생하는 손해에 대해서는 회사는 책임지지 않습니다.
                                회사는 바이어 회원과 프리랜서 회원이 상호 협의하여 지정한 입금일시와 입금방법 등에 따라 바이어 회원의 입금 또는 대금결제를 위해 필요한 정보를 확인하며, 바이어 회원으로부터 프로젝트 대금이 입금되면 즉시 프리랜서에게 입금현황을 통지하고, 바이어 회원이 입금한 후 3영업일 이내에 프리랜서 회원에게 중개수수료 및 제세공과금을 제외한 프로젝트 대금을 입금합니다.
                                프로젝트 등록 및 입찰과정 - 재택(턴키) 서비스
                                바이어 회원이 프로젝트 또는 서비스의 등록을 하고자 할 경우, 프로젝트나 서비스를 자세히 살펴본 후 이용 혹은 등록의사가 있을 때만 해야 합니다.
                                회원은 서비스 화면을 통해 바이어 또는 프리랜서 회원의 신용도를 조회할 수도 있습니다.
                                바이어 회원은 입찰마감 전 언제든지 입찰한 프리랜서 회원 현황을 확인할 수 있으며, 프로젝트를 수행할 프리랜서 회원을 선정을 할 수 있습니다.
                                프리랜서 회원은 지정된 입찰 마감일시까지 여러 번 입찰할 수 있으나, 연속적인 중복 입찰은 허용되지 않습니다.
                                입찰가격은 현재가에서 선정된 최저가 이상의 금액부터 자유롭게 책정할 수 있습니다.
                                바이어 회원과 프리랜서 회원은 회사에서 제공하는 채팅 등의 수단을 사용하여 프로젝트 수행에 대한 각종 사항을 협의 및 공유할 수 있습니다. <br><br>채팅방에 있는 모든 내용은 안전한 거래를 위하여 회사가 열람할 수 있습니다. <br><br>회원 간의 분쟁 발생 시, 회사는 관련 기록을 관련 수사 기관 등에 공유할 수 있습니다.
                                바이어 회원은 입찰에 참가한 프리랜서에 대하여 서류전형, 면접전형 등의 합격, 불합격 여부와 합격, 불합격 사유를 고지해야 합니다.
                                바이어 회원은 프리랜서 회원을 선정한 후에는 제13조 회사의 매매 보호 장치인 에스크로 제도를 이용하여야 합니다. <br><br>이때, 바이어 회원과 프리랜서 회원은 입금방법, 입금은행, 입금자명, 입금일시 등 입금정보를 전화, 이메일 혹은 서비스 화면에 기입해야 하며, 이를 잘못 기입하거나 기입하지 않음으로 인하여 발생하는 손해에 대해서는 회사는 책임지지 않습니다.
                                바이어 회원과 프리랜서 회원은 상호 합의하여 프로젝트 프로젝트 대금, 프로젝트 대금의 입금일시 등을 결정할 수 있습니다. <br><br>다만, 본 약관의 제 13 조에서 정의한 절차를 따라야 합니다.
