                                제 14 조 (평가기간)
                                “평가기간”이라 함은 프로젝트 최종 낙찰자인 프리랜서 회원이 프로젝트를 진행함에 있어서 적합한 개발 능력이 있는 지의 여부를 판단하는 기간을 말합니다. <br><br>이 평가 기간 이후에 대한 책임은 프리랜서 회원의 특별한 귀책사유가 없는 한 바이어 회원에게 있는 것으로 추정합니다.
                                평가기간의 기능(목적)은 다음과 같습니다.
                                1. <br><br>평가기간 동안에 바이어 회원이 프로젝트 진행에 있어서 프리랜서 회원이 적합한 개발 능력이 있는 지의 여부를 판단하고, 이에 적합하지 않다고 판단되어질 때 회사에게 다른 인력의 추천을 요구하여 프로젝트가 원만하게 진행할 수 있도록 하기 위한 기능
                                2. <br><br>평가기간 이후에 프로젝트 의뢰자인 바이어 회원이 프로젝트 수주자인 프리랜서 회원의 개발 능력 부족 등을 이유로 프로젝트 대금을 지급하지 않음을 방지하기 위한 기능
                                바이어 회원과 프리랜서 회원은 신의성실의 원칙에 입각하여 프로젝트 기간, 프로젝트 내용 등을 종합적으로 판단하여 상호 협의하여 평가 기간을 정하며, 그 기간 동안의 프로젝트 대금은 프로젝트의 진행 정도, 프로젝트에 투입된 일수 등을 종합적으로 판단하여 회사의 중개 수수료를 포함하여 기 지급하기로 약정했던 금원의 50% 이상으로 합니다. <br><br>단, 프로젝트 성격상 평가기간이 필요 없는 경우에는 그러하지 않습니다.

